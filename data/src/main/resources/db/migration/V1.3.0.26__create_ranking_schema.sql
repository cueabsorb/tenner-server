CREATE TABLE IF NOT EXISTS `ir_profile`.`ranking_snapshots` (
    `id` CHAR(36) NOT NULL COMMENT 'UUID v4',
    `updated_at` DATE NOT NULL COMMENT '榜单更新时间',
    `next_refresh_at` DATE NOT NULL COMMENT '预计下次更新时间',
    `status` TINYINT NOT NULL DEFAULT 0 COMMENT '数据状态: 0=正常, -1=删除',
    `created_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `modified_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (`id`),
    KEY `idx_ranking_snapshots_updated_at` (`updated_at` DESC),
    KEY `idx_ranking_snapshots_status` (`status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='排行榜快照';

CREATE TABLE IF NOT EXISTS `ir_profile`.`world_ranking_players` (
    `id` CHAR(36) NOT NULL COMMENT 'UUID v4',
    `snapshot_id` CHAR(36) NOT NULL COMMENT 'ranking_snapshots.id',
    `gender` ENUM('men','women') NOT NULL COMMENT '男子/女子',
    `rank_no` SMALLINT NOT NULL COMMENT '世界排名',
    `name` VARCHAR(120) NOT NULL COMMENT '球员姓名',
    `country` VARCHAR(80) NULL COMMENT '国家/地区',
    `points` INT NULL COMMENT '排名积分',
    `source` VARCHAR(20) NOT NULL COMMENT '数据来源: ATP/WTA',
    `avatar_url` VARCHAR(512) NULL COMMENT '头像URL，NULL时客户端使用App默认logo',
    `status` TINYINT NOT NULL DEFAULT 0 COMMENT '数据状态: 0=正常, -1=删除',
    `created_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `updated_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (`id`),
    UNIQUE KEY `uk_world_ranking_snapshot_gender_rank` (`snapshot_id`, `gender`, `rank_no`),
    KEY `idx_world_ranking_gender_rank` (`gender`, `rank_no`),
    KEY `idx_world_ranking_status` (`status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='世界网球排名';

