-- IRallyIn: court change requests.
-- Existing courts are edited through this temporary review table first.
CREATE TABLE IF NOT EXISTS `ir_profile`.`court_change_requests` (
    `id`                         CHAR(36)        NOT NULL COMMENT 'UUID v4',
    `court_id`                   CHAR(36)        NOT NULL COMMENT '关联courts.id',
    `country`                    VARCHAR(100)    NOT NULL COMMENT '国家/地区',
    `city`                       VARCHAR(100)    NOT NULL COMMENT '城市',
    `name`                       VARCHAR(200)    NOT NULL COMMENT '申请修改后的球场名',
    `address`                    VARCHAR(500)    NULL COMMENT '申请修改后的地址',
    `contact_phone`              VARCHAR(40)     NULL COMMENT '申请修改后的联系电话',
    `wechat_mini_program_name`   VARCHAR(100)    NULL COMMENT '申请修改后的微信小程序名称',
    `photo_urls`                 JSON            NULL COMMENT '申请修改后的球场照片URL数组',
    `description`                TEXT            NULL COMMENT '申请修改后的球场补充信息',
    `latitude`                   DECIMAL(10, 7)  NULL COMMENT '申请修改后的纬度',
    `longitude`                  DECIMAL(10, 7)  NULL COMMENT '申请修改后的经度',
    `surface_type`               VARCHAR(200)    NULL COMMENT '申请修改后的地面类型',
    `indoor_outdoor`             ENUM('indoor','outdoor','both') NULL COMMENT '申请修改后的室内外',
    `has_indoor`                 TINYINT(1)      NOT NULL DEFAULT 0 COMMENT '申请修改后是否有室内场地',
    `has_outdoor`                TINYINT(1)      NOT NULL DEFAULT 0 COMMENT '申请修改后是否有室外场地',
    `total_court_count`          INT             NULL COMMENT '申请修改后的球场总片数',
    `opening_time`               TIME            NULL COMMENT '申请修改后的每日营业开始时间',
    `closing_time`               TIME            NULL COMMENT '申请修改后的每日营业结束时间',
    `request_status`             ENUM('pending','approved','voided','blacklisted') NOT NULL DEFAULT 'pending' COMMENT '修改申请审核状态',
    `submitted_by`               CHAR(36)        NOT NULL COMMENT '提交修改申请的用户ID',
    `reviewed_by`                VARCHAR(100)    NULL COMMENT '审核人',
    `reviewed_at`                DATETIME        NULL COMMENT '审核时间',
    `rejected_reason`            VARCHAR(500)    NULL COMMENT '拒绝/作废原因',
    `created_at`                 TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `updated_at`                 TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    `status`                     TINYINT         NOT NULL DEFAULT 0 COMMENT '数据状态: 0=正常, -1=删除',
    `pending_court_id`           CHAR(36) GENERATED ALWAYS AS (CASE WHEN `request_status` = 'pending' AND `status` = 0 THEN `court_id` ELSE NULL END) STORED,
    PRIMARY KEY (`id`),
    UNIQUE KEY `uk_court_change_pending` (`pending_court_id`),
    KEY `idx_court_change_court` (`court_id`, `created_at` DESC),
    KEY `idx_court_change_status` (`request_status`, `created_at` DESC)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='网球场信息修改审核临时表';
